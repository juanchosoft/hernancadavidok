                            <h5>junio 29, 2023</h5>
                            <p>Desde el 15 de julio, entrará en vigor en Colombia la Ley 2101 de 2021, la cual tiene como objetivo reducir la jornada laboral a 42 horas semanales paulatinamente hasta 2026. En entrevista con Mañanas Blu, cuando Colombia está al aire, Hernán Cadavid, representante del Centro Democrático, habló sobre esta medida y aclaró algunos puntos importantes.</p>
                            <p>Cadavid destacó que este logro es para todos los trabajadores colombianos y trasciende las diferencias partidistas: “Tienen la intención de invisibilizar este tipo de logros que trascienden inclusive de ser de un partido y es un logro para todos los trabajadores en Colombia, eso es lo primero. Lo segundo, es que esto no se hubiera podido lograr si no es con el consenso también con los sectores productivos, económicos , generadores de empleos en Colombia”.</p>
                            <p>La reducción de la jornada laboral se implementará de manera gradual hasta el año 2026. A partir de este mes de julio, se reducirá una hora a la semana, y cada año se continuará disminuyendo en una hora adicional. No será necesario que los trabajadores presenten ninguna solicitud, ya que la ley establece de manera clara que la jornada se reduce automáticamente.</p>
                            <p>El representante aclaró que la ley aplica a todos los trabajadores cubiertos por el régimen laboral colombiano, excluyendo el sector público con regímenes de aplicación distintos y aquellos trabajadores oficiales del Estado sujetos a convenciones colectivas.</p>
                            <br>
                            <p>¿Reducción de jornada laboral disminuye la productividad?</p>
                            <br>
                            <p>Una de las preocupaciones planteadas fue la posible disminución de la productividad debido a la reducción de la jornada laboral. Cadavid mencionó que empresas que ya habían adoptado esta medida de forma voluntaria encontraron beneficios como una disminución en permisos y licencias médicas, así como una mayor motivación del trabajador al tener más tiempo para compartir con su familia. Aunque reconoció que no todos los sectores serán iguales.</p>
                            <br>
                            <p>Beneficios se mantienen</p>
                            <br>
                            <p>El representante reiteró que no se perderán beneficios existentes, como el día de la familia, debido a la entrada en vigencia de la ley. Aclaró que se podrá llegar a acuerdos con los empleadores sobre cómo manejar esas horas destinadas al día de la familia, pero sin afectar los beneficios y salarios de los trabajadores.</p>
                            <br>
                            <p>“Lo que se ha propuesto es que se pueda conversar y llegar a un acuerdo con el empleador sobre cómo tramitar esas horas de día de la familia, pero eso no representa bajo ninguna circunstancia que se vayan a disminuir o a tocar siquiera factores prestacionales como vacaciones, primas, salarios, todo queda tal cual y conforme existe hoy. Lo único que se reduce es la jornada laboral, no los beneficios del trabajador o su salario merecido”, aclaró.</p>
                           
